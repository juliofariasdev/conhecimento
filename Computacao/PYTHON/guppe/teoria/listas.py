# for indice, item in enumerate(lista) -> gerar o índice e o item correspondente ao mesmo.

# lista.index(item, indice de referncia) -> encontrar o índice do primeiro 'item' na lista, apartir do indice de referencia que por padrão é 0.

# sum(lista) -> soma os valores da lista

# max(lista) -> informa o item com maior valor númerico

